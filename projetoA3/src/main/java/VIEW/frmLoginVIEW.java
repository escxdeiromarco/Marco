    private void Logar() {
        try {

            String nomeusuario;
            String senha;

            nomeusuario = txtusuarioLogin.getText();
            senha = txtsenhaUsuario.getText();

            UsuarioDTO objusuariodto = new UsuarioDTO();
            objusuariodto.setNomeusuario(nomeusuario);
            objusuariodto.setSenha(senha);

            UsuarioDAO objusuariodao = new UsuarioDAO();
            ResultSet rsusuariodao = objusuariodao.autenticacaoUsuario(objusuariodto);

            if (rsusuariodao.next()) {
                //chamar tela
                principalVIEW objfrmprincipalview = new principalVIEW();
                objfrmprincipalview.setVisible(true);

                dispose();
            } else {
                //enviar mensagem de incorreto
                JOptionPane.showMessageDialog(null, "Usu√°rio ou senha inv√°lidos");
            }

        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "frmLoginVIEW" + erro);
        }
    }

