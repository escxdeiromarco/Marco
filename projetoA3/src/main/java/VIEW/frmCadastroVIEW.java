    private void cadBotaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadBotaoActionPerformed

        String nomeUsuario;
        String nomecompleto;
        String eMail;
        String telefone;
        String senha;
        
        nomeUsuario = cadNomeusuario.getText();
        nomecompleto = cadNomecompleto.getText();
        eMail = cadEmail.getText();
        telefone = cadTelefone.getText();
        senha = cadSenha.getText();
        
        UsuarioDTO objcadusuariodto = new UsuarioDTO();
        objcadusuariodto.setNomeusuario(nomeUsuario);
        objcadusuariodto.setNomecompleto(nomecompleto);
        objcadusuariodto.seteMail(eMail);
        objcadusuariodto.setTelefone(telefone);
        objcadusuariodto.setSenha(senha);
        
        CadastroDAO objcadusuariodao = new CadastroDAO();
        objcadusuariodao.cadastroUsuario(objcadusuariodto);
        
        
        
        
        
        
        
        
        
    }//GEN-LAST:event_cadBotaoActionPerformed

