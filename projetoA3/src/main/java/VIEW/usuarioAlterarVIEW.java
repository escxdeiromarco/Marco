    private void AlterarUsuario() {

        String nomeUsuario;
        String nomecompleto;
        String eMail;
        String telefone;
        String senha;

        nomeUsuario = usuarioAlterar.getText();
        nomecompleto = nomeAlterar.getText();
        eMail = emailAlterar.getText();
        telefone = telefoneAlterar.getText();
        senha = senhaAlterar.getText();

        UsuarioDTO objalterardto = new UsuarioDTO();
        objalterardto.setNomeusuario(nomeUsuario);
        objalterardto.setNomecompleto(nomecompleto);
        objalterardto.seteMail(eMail);
        objalterardto.setTelefone(telefone);
        objalterardto.setSenha(senha);
        

        CadastroDAO objalterarcaddao = new CadastroDAO();
        objalterarcaddao.alterarUsuario(objalterardto);

    }

